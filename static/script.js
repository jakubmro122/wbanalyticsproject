function goTo(){
    value = document.getElementById('select').value
    if(value == "one"){
        location.href="/oneDay";
    }
    else if(value == "seven"){
        location.href="/sevenDays/181";
    }
    else if(value == "thirty"){
        location.href="/thirtyDays/181";
    }
    else if (value == "all"){
        location.href="/fullData/181";
    }
}

